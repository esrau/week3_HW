package week3;

public class courses {
    String courseName;
    int credit = 0;
    int studentCount = 0;

    public courses()
    {
        this.courseName = null;
        this.credit = credit;
        this.studentCount = studentCount;
    }

    courses(String courseName, int credit, int studentCount){
        this.courseName = courseName;
        this.credit = credit;
        this.studentCount = studentCount;
    }

}
